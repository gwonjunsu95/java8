package Day19;

public class PhoneNumber {

	String name;
	int number;
	
	public PhoneNumber(String name, int number) {
		super();
		this.name = name;
		this.number = number;
	}
	
	
	public String getName() {
		return name;
	}
	public int getNumber() {
		return number;
	}
	
	
}
